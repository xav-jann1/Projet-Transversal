void enveloppe_signal(void){
	if (enable_reception==true){
 		HAL_ADC_Start (&hadc2);
		HAL_ADC_PollForConversion(&hadc2,10);
		//ADCValue_prev3=ADCValue_prev2;
		ADCValue_prev2=ADCValue_prev;
		ADCValue_prev=ADCValue;
		ADCValue = HAL_ADC_GetValue(&hadc2);
		

	if (ADCValue==0x000 && ADCValue_prev==0x000 && ADCValue_prev2==0x000){
		//si  toutes les valeurs précédentes et actuelles sont à 0, on met le seuil à 0
		MaxValue=0x000;
	}
	else{
		if(ADCValue>MaxValue){
		MaxValue=ADCValue;
		}
	}
	HAL_DAC_SetValue(&hdac,DAC_CHANNEL_1,DAC_ALIGN_12B_R, MaxValue);
	HAL_DAC_Start(&hdac,DAC_CHANNEL_1);
	}
}
