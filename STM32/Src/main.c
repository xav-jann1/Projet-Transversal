void interprete_commande(unsigned char* cmd){
	int code_freq=6;
	if(cmd[0]=='S' && cmd[1]=='D'){
		enable_reception=false;
		sscanf((char*) cmd, "SD F:%d P:%d W:%d B:%d ", &code_freq, &son , &silence , &bip);
		freq = tab_freq[code_freq-1];//on fait la correspondance entre le numero de frequence et la fréquence
		init_generation_son(freq, son, silence, bip);
	}
	else if(cmd[0]=='A' && cmd[1]=='S' && cmd[2]=='S'){
		enable_reception = true;
		init_acquisition_son();
		//acquisition_signaux();
	}
	
}


